CREATE TABLE IF NOT EXISTS timetable_entries (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  timetable_id INTEGER NOT NULL,
  class_name TEXT NOT NULL,
  day_of_week INTEGER NOT NULL,
  session TEXT NOT NULL,
  period INTEGER NOT NULL,
  subject TEXT,
  CONSTRAINT fk_timetable_entries_timetable_id FOREIGN KEY (timetable_id) REFERENCES timetables(id) ON DELETE CASCADE,
  CONSTRAINT fk_timetable_entries_class_name FOREIGN KEY (class_name) REFERENCES classes(name) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS weekly_scores (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  week_id INTEGER NOT NULL,
  class_name TEXT NOT NULL,
  score DOUBLE PRECISION,
  updated_at TEXT,
  CONSTRAINT fk_weekly_scores_week_id FOREIGN KEY (week_id) REFERENCES schedule_weeks(id) ON DELETE CASCADE,
  CONSTRAINT fk_weekly_scores_class_name FOREIGN KEY (class_name) REFERENCES classes(name) ON DELETE CASCADE
