CREATE TABLE IF NOT EXISTS classes (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT UNIQUE,
  grade INTEGER,
  is_active INTEGER DEFAULT 1
);

CREATE TABLE IF NOT EXISTS rules (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category TEXT,
  name TEXT,
  score_delta INTEGER
);

CREATE TABLE IF NOT EXISTS admins (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username TEXT UNIQUE,
  password TEXT
);

CREATE TABLE IF NOT EXISTS schedule_weeks (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  week_number INTEGER,
  start_date TEXT,
  end_date TEXT,
  created_at TEXT
);

CREATE TABLE IF NOT EXISTS accounts (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  class_id INTEGER UNIQUE,
  password_gvcn TEXT,
  password_bcs TEXT,
  password_codo TEXT,
  pin_bcs TEXT,
  pin_failed_attempts INTEGER DEFAULT 0,
  pin_locked_until BIGINT DEFAULT 0,
  password_changed INTEGER DEFAULT 0,
  created_at TEXT,
  password_changed_gvcn INTEGER DEFAULT 0,
  password_changed_bcs INTEGER DEFAULT 0,
  password_changed_codo INTEGER DEFAULT 0,
  CONSTRAINT fk_accounts_class_id FOREIGN KEY (class_id) REFERENCES classes(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS schedule_assignments (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  week_id INTEGER NOT NULL,
  red_class TEXT NOT NULL,
  duty_class TEXT NOT NULL,
  CONSTRAINT fk_schedule_assignments_week_id FOREIGN KEY (week_id) REFERENCES schedule_weeks(id) ON DELETE CASCADE,
  CONSTRAINT fk_schedule_assignments_red_class FOREIGN KEY (red_class) REFERENCES classes(name) ON DELETE CASCADE,
  CONSTRAINT fk_schedule_assignments_duty_class FOREIGN KEY (duty_class) REFERENCES classes(name) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS duty_sessions (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  week_id INTEGER NOT NULL,
  date TEXT,
  red_class TEXT NOT NULL,
  duty_class TEXT NOT NULL,
  status TEXT DEFAULT 'draft' CHECK (status IN ('draft', 'signed')),
  created_at TEXT,
  signed_at TEXT,
  signed_snapshot_hash TEXT,
  CONSTRAINT fk_duty_sessions_week_id FOREIGN KEY (week_id) REFERENCES schedule_weeks(id) ON DELETE CASCADE,
  CONSTRAINT fk_duty_sessions_red_class FOREIGN KEY (red_class) REFERENCES classes(name) ON DELETE CASCADE,
  CONSTRAINT fk_duty_sessions_duty_class FOREIGN KEY (duty_class) REFERENCES classes(name) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS duty_violations (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_id INTEGER NOT NULL,
  rule_id INTEGER NOT NULL,
  quantity INTEGER,
  note TEXT,
  CONSTRAINT fk_duty_violations_session_id FOREIGN KEY (session_id) REFERENCES duty_sessions(id) ON DELETE CASCADE,
  CONSTRAINT fk_duty_violations_rule_id FOREIGN KEY (rule_id) REFERENCES rules(id) ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS duty_signatures (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_id INTEGER NOT NULL,
  photo_path TEXT,
  signed_at TEXT,
  CONSTRAINT fk_duty_signatures_session_id FOREIGN KEY (session_id) REFERENCES duty_sessions(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS duty_revision_logs (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_id INTEGER NOT NULL,
  action TEXT,
  created_at TEXT,
  CONSTRAINT fk_duty_revision_logs_session_id FOREIGN KEY (session_id) REFERENCES duty_sessions(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS week_closings (
  week_id INTEGER PRIMARY KEY,
  closed_at TEXT,
  CONSTRAINT fk_week_closings_week_id FOREIGN KEY (week_id) REFERENCES schedule_weeks(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS daily_bonus (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_id INTEGER,
  week_id INTEGER,
  date TEXT,
  class_name TEXT,
  points DOUBLE PRECISION,
  min_score DOUBLE PRECISION,
  all_above_9 INTEGER,
  source TEXT,
  created_at TEXT,
  updated_at TEXT,
  periods_json TEXT,
  CONSTRAINT uq_daily_bonus_week_date_class UNIQUE (week_id, date, class_name),
  CONSTRAINT fk_daily_bonus_session_id FOREIGN KEY (session_id) REFERENCES duty_sessions(id) ON DELETE CASCADE,
  CONSTRAINT fk_daily_bonus_week_id FOREIGN KEY (week_id) REFERENCES schedule_weeks(id) ON DELETE CASCADE,
  CONSTRAINT fk_daily_bonus_class_name FOREIGN KEY (class_name) REFERENCES classes(name) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS bonus_uploads (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  week_id INTEGER,
  grade TEXT,
  file_name TEXT,
  xlsx_count INTEGER,
  uploaded_at TEXT,
  CONSTRAINT fk_bonus_uploads_week_id FOREIGN KEY (week_id) REFERENCES schedule_weeks(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS timetables (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
